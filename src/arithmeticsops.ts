// +, -, *, /, %, ++, --

var numOne: number = 1;
var numTwo: number = 2;

// Addition 
console.log('Addition');
console.log(numOne + numTwo);

// Subtraction 
console.log('Subtraction');
console.log(numOne - numTwo);

// Multiplication 
numOne = 10;
numTwo = 15;
console.log('Multiplication');
console.log(numOne * numTwo);

// Division 
console.log('Division');
console.log(numOne / numTwo);

// Modulus 
console.log('Modulus');
var numThree: number = 3;
var numFour: number = 4;
console.log(numFour % numThree);

// Decrementor 
console.log('Decrementor');
var x: number = 10;
while (x > 0) {
    console.log(x);
    x--;
}
